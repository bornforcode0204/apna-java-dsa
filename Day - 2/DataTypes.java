public class DataTypes {
    public static void main(String[] args) {
        short a = 230;
        System.out.println(a);
        byte b = 23;
        System.out.println(b);
        char ch = 'a';
        System.out.println(ch);
        boolean var = true;
        System.out.println(var);
        int number = 11;
        System.out.println(number);
        float price = 21.2f;
        System.out.println(price);
        // long
        // double
    }
}


